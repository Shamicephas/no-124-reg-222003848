class unaryop{//class name
    public static void main(String args[]){//main class
        int a=7; //integer variable a
        int b=13;//integer variable b
        System.out.println(a+b);//7+13
        System.out.println(a++ + b++);//7(8)+13(14)
        System.out.println(a++ + ++b);//8+15
    }
}