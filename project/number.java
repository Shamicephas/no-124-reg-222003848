class number{//class name
    public static void main(String args[]){//main class
        int a=13;//integer variable a
        int b=-13;//integer variable b
        boolean c=true;
        boolean d=false;
        System.out.println(~a);//-14(positive of total minus, positive starts from 0)
        System.out.println(~b);//13(minus of total positive value starts from 0)
        System.out.println(!c);//opposite of boolean value
        System.out.println(!d);//opposite of boolean value
    }
}