//A java program to calculate the sum of natural numbers
//using for loop statement
class op{//class name
    public static void main(String[]args){//main class
        int sum=0;
        int n=15;
 
        //for loop
        for(int i=1;i<=n;i++){
            // sum of  numbers from 1 up to n = 15
            sum=sum+i;
        }
        System.out.println("Sum =  "+sum);//output
    }
}